# 2D list in python
my_list = [[1, 2, 3], [4, 5, 6], [7, 8, 9]]
print(my_list)  # prints [[1, 2, 3], [4, 5, 6], [7, 8, 9]]
# Accessing elements in a 2D list
print(my_list[0][0])  # prints 1
print(my_list[1][2])  # prints 6    

drinks = ["coffee", "tea", "juice"]
dinner = ["pizza", "pasta", "salad"]
dessert = ["cake", "ice cream", "pie"]
food = [drinks, dinner, dessert]
print(food)  # prints [['coffee', 'tea', 'juice'], ['pizza', 'pasta', 'salad'], ['cake', 'ice cream', 'pie']]