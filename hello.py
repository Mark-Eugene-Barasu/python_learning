my_numb = 10
print(max(my_numb, 5)) # This will print the maximum of my_numb and 5, which is 10.
print(min(my_numb, 5)) # This will print the minimum of my_numb and 5, which is 5.
print(abs(-5)) # This will print the absolute value of -5, which is 5.
print(pow(2, 3))    # This will print 2 raised to the power of 3, which is 8.
print(round(3.14159, 2)) # This will print 3.14159 rounded to 2 decimal places, which is 3.14.